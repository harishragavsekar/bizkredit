package com.bizkredit.common.enums;

// Loan product types supported by BizKredit
public enum ProductType {
    TERM_LOAN,              // Fixed amount, fixed tenure
    WORKING_CAPITAL_CC,     // Cash credit for working capital
    OVERDRAFT_FACILITY,     // Overdraft against collateral
    INVOICE_FINANCING,      // Finance against invoices/receivables
    EQUIPMENT_LOAN          // Loan for purchasing equipment
}
